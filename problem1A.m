a = imread('test20.jpg');
gray_scaleImage= rgb2gray(a);
new = imresize(gray_scaleImage,[512,512]);
subplot(2,3,1);
imshow(new);
title('original image');
b = imresize(new,.5);
subplot(2,3,2);
imshow(b);
title('256x256 image');
c = imresize(b,.5);
subplot(2,3,3);
imshow(c);
title('128x128 image');
d = imresize(c,.5);
subplot(2,3,4);
imshow(d);
title('64x64 image');
e = imresize(d,.5);
subplot(2,3,5);
imshow(e);
title('32x32 image');
